def daily_dir(date: str = None) -> Path:
    """返回今日数据目录 output/YYYYMMDD/，不存在时自动创建"""
    tag = date or datetime.now().strftime("%Y%m%d")
    d = OUTPUT_DIR / tag
    d.mkdir(parents=True, exist_ok=True)
    return d

